# Sobrecarga de operadores, el mismo operador trabaja de distintas maneras

a = 3
b = 5
print(a + b)

# En un caso hace una suma y en el otro una concatenación

a = 'Hola'
b = 'Mundo'
print(a + b)

a = [3, 4, 5]
b = [6, 7, 8, 9]
print(a + b)

